char	*ft_strcpy(char *dest, char *src)
{
	int d;
	int s;

	d = 0;
	s = 0;
	while (src[s] != '\0')
	{
		dest[d] = src[s];
		d++;
		s++;
	}
	dest[d] = '\0';
	return (dest);
}
