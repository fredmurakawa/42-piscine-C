int		ft_is_prime(int nb)
{
	int		n;

	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (0);
	n = 2;
	while (n <= nb / 2)
	{
		if (nb % n == 0)
			return (0);
		n++;
	}
	return (1);
}
